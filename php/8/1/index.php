      <li>&#9866; <b>r+</b>: открывает файл  для записи/чтения;</li>
      <li>&#9866; <b>w+</b>: открывает файл для записи/чтения. Удаляет содержимое файла или создает новый файл, если он до этого не существовал;</li>
      <li>&#9866; <b>a+</b>: открывает файл для записи/чтения. Создает новый файл, если он до этого не существовал;</li>
