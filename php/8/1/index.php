    <div class="div_head" id="head_2">Запись в файл</div>
    <div class="div_main">Для записи в файл используйте функцию <b>fwrite()</b>. Первым параметром функции <b>fwrite()</b> явялется <b>файл</b> для записи, вторым - <b>строка</b>, которую необходимо записать.</div>
    <div class="div_main">Нижеуказанный пример записывает пару имен в новый файл, который называется <b>names.txt</b></div>
    <div class="div_logo" id="php_logo">PHP</div>
    <div class="div_code">
        &lt;?php<br>
        $myfile = fopen("names.txt", "w");<br>
        $txt = "John\n";<br>
        $fwrite($myfile, $txt);<br>
        $txt = "David\n";<br>
        $fwrite($myfile, $txt);<br>
        $fclose();<br>
        // Текст в файл:<br>
        John<br>
        David<br>
        ?&gt;
    </div>
    <div class="div_main">Обратите внимание, что мы записывали в файл <b>names.txt</b> дважды, а затем мы использовали функцию <b>fclose()</b>, чтобы закрыть файл</div>
    <div class="div_logo">&#9745;</div>
    <div class="div_fix_main">Символ <b>\n</b> используется для записи <b>новых строк</b>.</div>

